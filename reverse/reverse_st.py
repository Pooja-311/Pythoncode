def reverse_st(st):
 rst = ""
 for i in st:
    if i.isalpha():
        rst = i + rst

 st = list(st)
 rst = list(rst)
 final = []
 l = len(st)
 j =0

 for i in range(l):
    if st[i].isalpha():
        final.append(rst[j])
        j = j+1
    else:
        final.append(st[i])


 return ((''.join(final)))
